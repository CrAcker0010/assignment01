int main() {
    int decimal_value;
    
    // Input from the user
    printf("Enter a decimal number: ");
    scanf("%d", &decimal_value);
    
       // Convert to octal
    printf("Octal: %o\n", decimal_value);
    
    // Convert to hexadecimal
    printf("Hexadecimal: %X\n", decimal_value);
   
}
